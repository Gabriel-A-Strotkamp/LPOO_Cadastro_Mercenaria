    public void carregarPedidos(){
        DefaultListModel modelo = new DefaultListModel();
        modelo.removeAllElements();
        
        for(Pedido p: listaPedidos){
            modelo.addElement(p);
        }
        
        lstPedidos.setModel(modelo);
    }
    
