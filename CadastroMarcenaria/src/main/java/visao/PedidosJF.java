    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        Pedido pedidoSelecionado = lstPedidos.getSelectedValue();
        if(pedidoSelecionado != null){
            int opRomover = JOptionPane.showConfirmDialog(rootPane,
                "Tem certeza que deseja remover o Pedido: "+pedidoSelecionado.getId()+"?");
            if(opRomover == JOptionPane.YES_OPTION){
                listaPedidos.remove(pedidoSelecionado);
                carregarPedidos();
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "Selecione um Pedido para remover!!");
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        Pedido pedidoSelecionado = lstPedidos.getSelectedValue();
        if(pedidoSelecionado != null){
            CadastroPedidoJD telaCadastro = new CadastroPedidoJD(this, true);
            telaCadastro.setPedido(pedidoSelecionado);
            telaCadastro.setVisible(true);
            carregarPedidos();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Selecione um Pedido para Editar!!");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        CadastroPedidoJD telaCadastro = new CadastroPedidoJD(this, true);
        telaCadastro.setVisible(true);

        Pedido pedidoNovo = telaCadastro.getPedido();

        PedidoController control = new PedidoController();
        listaPedidos = (ArrayList<Pedido>) control.addPedido(pedidoNovo, listaPedidos);
        carregarPedidos();
    }//GEN-LAST:event_btnAdicionarActionPerformed

