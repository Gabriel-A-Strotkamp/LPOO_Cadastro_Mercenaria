    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        CadastroClienteJD telaCadastro = new CadastroClienteJD(this, true);
        telaCadastro.setVisible(true);

        Cliente clienteNovo = telaCadastro.getCliente();

        ClienteController control = new ClienteController();
        listaClientes = (ArrayList<Cliente>) control.addCliente(clienteNovo, listaClientes);
        carregarClientes();
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        Cliente clienteSelecionado = lstClientes.getSelectedValue();
        if(clienteSelecionado != null){
            int opRomover = JOptionPane.showConfirmDialog(rootPane,
                "Tem certeza que deseja remover o Cliente: "+clienteSelecionado.getNome()+"?");
            if(opRomover == JOptionPane.YES_OPTION){
                listaClientes.remove(clienteSelecionado);
                carregarClientes();
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "Selecione um Cliente para remover!!");
        }

    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        Cliente clienteSelecionado = lstClientes.getSelectedValue();
        if(clienteSelecionado != null){
            CadastroClienteJD telaCadastro = new CadastroClienteJD(this, true);
            telaCadastro.setCliente(clienteSelecionado);
            telaCadastro.setVisible(true);
            carregarClientes();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Selecione um Cliente para Editar!!");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

