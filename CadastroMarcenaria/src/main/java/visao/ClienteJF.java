    public void carregarClientes(){
        DefaultListModel modelo = new DefaultListModel();
        modelo.removeAllElements();
        
        for(Cliente c: listaClientes){
            modelo.addElement(c);
        }
        
        lstClientes.setModel(modelo);
    }
    
